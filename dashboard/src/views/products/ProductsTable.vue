<script setup lang="ts">
import { computed, onMounted, ref, Ref } from "vue";
import { useProductStore } from "../../store";
import Spinner from "../../components/core/Spinner.vue";
import TableHeaderCell from "../../components/core/table/TableHeaderCell.vue";
import { IProduct } from "../../types/product";
import { IMetaLink } from "../../types/commons";
import { PRODUCTS_PER_PAGE } from "../../constants";
import { Menu, MenuButton, MenuItem, MenuItems } from "@headlessui/vue";
import { EllipsisVerticalIcon, PencilIcon, TrashIcon } from "@heroicons/vue/24/outline";

const productStore = useProductStore();

const perPage: Ref<number> = ref(PRODUCTS_PER_PAGE);
const search: Ref<string> = ref('');
const products = computed(() => productStore.items);
const productsLoading = computed(() => productStore.loading);
//const productsLinks = computed(() => productStore.links);
const productsMeta = computed(() => productStore.meta);
const sortField: Ref<string> = ref('updated_at');
const sortDirection: Ref<string> = ref("desc");
// const product = ref({});
const showProductModal: Ref<boolean> = ref(false);
const emit = defineEmits(["clickEdit"]);

const annim: Ref<boolean> = ref(true);
const annimTrue = computed(() => annim.value);

onMounted(() => {
  getProducts();
})

const getForPage = (ev: any, link: IMetaLink) => {
  ev.preventDefault();
  if (!link.url || link.active) {
    return;
  }else{
    productStore.getItems({url: link.url})
  }
}

const getProducts = (url = null) => {
  productStore.getItems({
    url: url,
    search: search.value,
    per_page: perPage.value,
    sort_field: sortField.value,
    sort_direction: sortDirection.value
  });
  changeAnnim();
}

const sortProducts = (field: string) => {
  if (field === sortField.value) {
    if (sortDirection.value === "desc") {
      sortDirection.value = "asc"
    } else {
      sortDirection.value = "desc"
    }
  } else {
    sortField.value = field;
    sortDirection.value = "asc"
  }
  getProducts()
}

const showAddNewModal = () => {
  showProductModal.value = true
}

const deleteProduct = (product: IProduct) => {
  if (!confirm(`Are you sure you want to delete the product?`)) {
    return
  }
  if (product.id){
    productStore.removeItem(product.id);
  }else{
    return;
  }
}

const editProduct = (product: IProduct) => {
  emit("clickEdit", product);
}

const changeAnnim = () => {
  setTimeout(() => {
    annim.value = false;
  }, perPage.value * 100);
}
</script>

<template>
  <div class="bg-white p-4 rounded-lg shadow animate-fade-in-down">
    <div class="flex justify-between border-b-2 pb-3">
      <div class="flex items-center">
        <span class="whitespace-nowrap mr-3">Per Page</span>
        <select
					@change="getProducts(null)"
					v-model="perPage"
          class="appearance-none relative block w-24 px-3 py-2 border border-gray-300 placeholder-gray-500 text-gray-900 rounded-md focus:outline-none focus:ring-indigo-500 focus:border-indigo-500 focus:z-10 sm:text-sm"
				>
          <option value="5">5</option>
          <option value="10">10</option>
          <option value="20">20</option>
          <option value="50">50</option>
          <option value="100">100</option>
        </select>
        <span class="ml-3">Found {{productsMeta?.total}} products</span>
      </div>
      <div>
        <input
					v-model="search" @change="getProducts(null)"
          class="appearance-none relative block w-48 px-3 py-2 border border-gray-300 placeholder-gray-500 text-gray-900 rounded-md focus:outline-none focus:ring-indigo-500 focus:border-indigo-500 focus:z-10 sm:text-sm"
          placeholder="Type to Search products"
				>
      </div>
    </div>

    <table class="table-auto w-full">
      <thead>
      <tr>
        <TableHeaderCell
					field="id"
					:sort-field="sortField"
					:sort-direction="sortDirection"
					@click="sortProducts('id')"
				>
          ID
        </TableHeaderCell>
        <TableHeaderCell
					field="image"
					:sort-field="sortField"
					:sort-direction="sortDirection"
				>
          Image
        </TableHeaderCell>
        <TableHeaderCell
					field="title"
					:sort-field="sortField"
					:sort-direction="sortDirection"
          @click="sortProducts('title')"
				>
          Title
        </TableHeaderCell>
        <TableHeaderCell
					field="price"
					:sort-field="sortField"
					:sort-direction="sortDirection"
          @click="sortProducts('price')"
				>
          Price
        </TableHeaderCell>
        <TableHeaderCell
					field="updated_at"
					:sort-field="sortField"
					:sort-direction="sortDirection"
          @click="sortProducts('updated_at')"
				>
          Last Updated At
        </TableHeaderCell>
        <TableHeaderCell field="actions">
          Actions
        </TableHeaderCell>
      </tr>
      </thead>
      <tbody v-if="productsLoading || !products.length">
      <tr>
        <td colspan="6">
          <Spinner v-if="productsLoading"/>
          <p v-else class="text-center py-8 text-gray-700">
            There are no products
          </p>
        </td>
      </tr>
      </tbody>
      <tbody v-else>
      <tr
				v-for="(product, index) of products"
				class=""
        :class="{'animate-fade-in-down': annimTrue}"
        :style="{'animation-delay': (index * 0.1) + 's'}"
			>
        <td class="border-b p-2 ">{{ product.id }}</td>
        <td class="border-b p-2 ">
          <img class="w-16 h-16 object-cover" :src="product.image" :alt="product.title">
        </td>
        <td class="border-b p-2 max-w-[200px] whitespace-nowrap overflow-hidden text-ellipsis">
          {{ product.title }}
        </td>
        <td class="border-b p-2">
          ${{ product.price }}
        </td>
        <td class="border-b p-2">
          {{ product.updated_at }}
        </td>
        <td class="border-b p-2">
          <Menu as="div" class="relative inline-block text-left">
            <div>
              <MenuButton
                class="inline-flex items-center w-full justify-center rounded-full h-10 bg-black bg-opacity-0 text-sm font-medium text-white hover:bg-opacity-5 focus:bg-opacity-5 focus:outline-none focus-visible:ring-2 focus-visible:ring-white focus-visible:ring-opacity-75"
              >
                <EllipsisVerticalIcon
                  class="h-5 w-5 text-indigo-500"
                  aria-hidden="true"
								/>
              </MenuButton>
            </div>

            <transition
              enter-active-class="transition duration-100 ease-out"
              enter-from-class="transform scale-95 opacity-0"
              enter-to-class="transform scale-100 opacity-100"
              leave-active-class="transition duration-75 ease-in"
              leave-from-class="transform scale-100 opacity-100"
              leave-to-class="transform scale-95 opacity-0"
            >
              <MenuItems
                class="absolute right-0 mt-2 w-32 z-10 origin-top-right divide-y divide-gray-100 rounded-md bg-white shadow-lg ring-1 ring-black ring-opacity-5 focus:outline-none"
              >
                <div class="px-1 py-1">
                  <MenuItem v-slot="{ active }">
                    <button
                      :class="[
                        active ? 'bg-indigo-600 text-white' : 'text-gray-900',
                        'group flex w-full items-center rounded-md px-2 py-2 text-sm',
                      ]"
                      @click="editProduct(product)"
                    >
                      <PencilIcon
                        :active="active"
                        class="mr-2 h-5 w-5 text-indigo-400"
                        aria-hidden="true"
                      />
                      Edit
                    </button>
                  </MenuItem>
                  <MenuItem v-slot="{ active }">
                    <button
                      :class="[
                        active ? 'bg-indigo-600 text-white' : 'text-gray-900',
                        'group flex w-full items-center rounded-md px-2 py-2 text-sm z-100',
                      ]"
                      @click="deleteProduct(product)"
                    >
                      <TrashIcon
                        :active="active"
                        class="mr-2 h-5 w-5 text-indigo-400"
                        aria-hidden="true"
                      />
                      Delete
                    </button>
                  </MenuItem>
                </div>
              </MenuItems>
            </transition>
          </Menu>
        </td>
      </tr>
      </tbody>
    </table>

    <div v-if="!productsLoading" class="flex justify-between items-center mt-5">
      <div v-if="products.length">
        Showing from {{ productsMeta?.from}} to {{ productsMeta?.to }}
      </div>
      <nav
        v-if="productsMeta?.total && productsMeta.per_page && productsMeta?.total > productsMeta.per_page"
        class="relative z-0 inline-flex justify-center rounded-md shadow-sm -space-x-px"
        aria-label="Pagination"
      >
        <!-- Current: "z-10 bg-indigo-50 border-indigo-500 text-indigo-600", Default: "bg-white border-gray-300 text-gray-500 hover:bg-gray-50" -->
        <a
          v-for="(link, i) in productsMeta.links"
          :key="i"
          :disabled="!link.url"
          href="#"
          @click="getForPage($event, link)"
          aria-current="page"
          class="relative inline-flex items-center px-4 py-2 border text-sm font-medium whitespace-nowrap"
          :class="[
              link.active
                ? 'z-10 bg-indigo-50 border-indigo-500 text-indigo-600'
                : 'bg-white border-gray-300 text-gray-500 hover:bg-gray-50',
              i === 0 ? 'rounded-l-md' : '',
              productsMeta.links && i === productsMeta.links.length - 1 ? 'rounded-r-md' : '',
              !link.url ? ' bg-gray-100 text-gray-700': ''
            ]"
          v-html="link.label"
        >
        </a>
      </nav>
    </div>
  </div>
</template>

